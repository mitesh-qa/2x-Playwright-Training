// Arithmetic Operators

let a = 10, b = 3;
console.log("value of a: ", +a);
console.log("value of b: ", +b);

let sum = a + b;
let sub = a - b;
let mul = a * b;
let div = a / b;

console.log("sum = a + b =", + sum);
console.log("sum = a - b =", + sub);
console.log("sum = a * b =", + mul);
console.log("sum = a / b =", + div);