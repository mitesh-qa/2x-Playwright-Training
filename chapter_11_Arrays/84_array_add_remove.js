let arr = [1, 2, 3];
console.log(arr);

arr.push(4);            // Add to END
console.log(arr);

arr.pop();              // Remove from END
console.log(arr);

arr.push(5, 6);         // Add multiple values at END
console.log(arr);

arr.unshift(0);         // Add to BEGINNING
console.log(arr);

arr.shift();            // Remove from BEGINNING
console.log(arr);



arr.unshift(100);       // Add any value to BEGINNING
console.log(arr);

arr.shift();            // Remove from BEGINNING
console.log(arr);