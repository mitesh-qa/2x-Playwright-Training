// && -> AND Gate
// || -> OR Gate

let a = true;
let b = false;
let c = true;
let d = false;

console.log(a && b);    // false
console.log(a && c);    // true
console.log(b && d);    // false

console.log(a || b);    // true
console.log(b || d);    // false    

console.log(!a);        // false
console.log(!b);        // true
